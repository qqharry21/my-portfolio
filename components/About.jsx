import { useTheme } from 'next-themes';

const About = () => {
  const { ref, inView } = useInView();

  const { theme } = useTheme();
  const isMobile = useMediaQuery({ query: '(max-width: 550px)' });
  const animation = useAnimation();
  const controls = useAnimation();

  const handlePan = (e, info) => {
    controls.set({
      rotateY: info.offset.x / 2,
      rotateX: -info.offset.y / 2,
    });
    // document.querySelector('.experience-panel').classList.add('cursor-grabbing');
  };

  const handlePanEnd = () => {
    controls.start({
      rotateY: 0,
      rotateX: 0,
    });

    // document.querySelector('.experience-panel').classList.remove('cursor-grabbing');
  };

  useEffect(() => {
    if (inView) {
      animation.start({
        opacity: 1,
        transition: {
          type: 'spring',
          duration: 2,
        },
      });
    }
    if (!inView) {
      animation.start({
        opacity: 0,
      });
    }
  }, [inView]);

  return (
    <motion.section className='section-container px-20 laptop:px-4 ' id='about' animate={animation}>
      <motion.h2 className='section-heading'>About me</motion.h2>
      <div className='about-content' ref={ref}>
        <motion.div className='about-left'>
          <p className='mb-4'>{about.description2}</p>

          <ul className='skill-list'>
            {about.skills.map((skill, index) => (
              <li key={index} className='skill-item'>
                {skill}
              </li>
            ))}
          </ul>
        </motion.div>
        <motion.div className='about-right'>
          <motion.div
            className={`about-wrapper group after:shadow-xl ${
              theme === 'light'
                ? 'border-primary-blue hover:border-gray-500'
                : 'border-main hover:border-primary-white'
            }`}
            animate={{ rotate: [2, -2] }}
            transition={{
              repeat: Infinity,
              repeatType: 'reverse',
              ease: 'easeInOut',
            }}>
            <Selfie theme={theme} isMobile={isMobile} />
            {/* <Image
              src='/black.svg'
              width={isMobile ? 210 : 384}
              height={isMobile ? 270 : 384}
              alt='Photo'
              className=' dark:bg-main group-hover:bg-primary-blue dark:group-hover:bg-primary-white bg-primary-dark/80 transition-colors duration-200 ease-in-out pointer-events-none'
            /> */}
          </motion.div>
        </motion.div>
      </div>
    </motion.section>
  );
};

